int	buf(int i, int nb)
{
	int p;

	p = 0;
	while (i != (nb - 1) && i < 46341)
	{
		if (((nb % i) == 0) && (nb == (i * i)))
		{
			p = i;
			break ;
		}
		else
			p = 1;
		i++;
	}
	return (p);
}

int	ft_sqrt(int nb)
{
	int i;
	int p;

	p = 0;
	i = 2;
	if (nb > 1)
	{
		p = buf(i, nb);
	}
	if (nb == 1)
	{
		p = 1;
	}
	if (nb == 0)
	{
		p = 0;
	}
	return (p);
}
