int		ft_is_prime(int nb)
{
	int i;
	int z;

	i = 2;
	z = 0;
	if (nb > 1)
	{
		while ((i != 46341) && (i < nb))
		{
			if ((nb % i) == 0)
				z = z + 1;
			i++;
		}
		if (z > 0)
			return (0);
		else
			return (1);
	}
	else
		return (0);
}
