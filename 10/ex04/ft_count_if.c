int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int buf;

	i = 0;
	buf = 0;
	while (tab[i] != '\0')
	{
		if ((*f)(tab[i]) == 1)
			buf = buf + 1;
		i++;
	}
	return (buf);
}
