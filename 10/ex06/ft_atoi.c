int	ft_atoi(char *str)
{
	int d;
	int c;
	int i;

	i = 0;
	c = -1;
	d = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
		c++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		i++;
		c++;
	}
	while (str[i] > 47 && str[i] < 58)
	{
		d = d * 10 + str[i] - 48;
		i++;
	}
	if (str[c] == '-')
		d = d * (-1);
	return (d);
}
