void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (1)
	{
		if (str[i] == ('\0'))
		{
			break ;
		}
		ft_putchar(str[i]);
		i++;
	}
}
