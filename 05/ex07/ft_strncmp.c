int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				buf;

	i = 0;
	buf = 0;
	if (n == 0)
		return (0);
	while (s1[i] != '\0' || s2[i] != '\0' || i < n)
	{
		if (s1[i] != s2[i])
		{
			buf = (s1[i] - s2[i]);
			break ;
		}
		if (s1[i] == s2[i] && i == (n - 1))
			return (0);
		i++;
	}
	return (buf);
}
