int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int buf;

	i = 0;
	buf = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			buf = (s1[i] - s2[i]);
			break ;
		}
		if (s1[i] == s2[i] && (s1[i + 1] == '\0' && s2[i + 1] == '\0'))
			return (0);
		i++;
	}
	return (buf);
}
