int		ft_str_is_alpha(char *str)
{
	int i;
	int buf;

	i = 0;
	buf = 0;
	if (str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (!((str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= 'a' && str[i] <= 'z')))
			buf = 1;
		i++;
	}
	if (buf == 0)
		return (1);
	return (0);
}
