int	ft_len_chisla(int nb)
{
	int	i;
	int	znak;

	znak = 0;
	i = 0;
	if (nb < 0)
		znak = 1;
	if (nb == 0)
		i = 1;
	if (nb == (-0))
		i = 1;
	else
		while (nb != 0)
		{
			nb = nb / 10;
			i++;
		}
	return (i + znak);
}
